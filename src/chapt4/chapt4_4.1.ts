// create two variables with a boolean value, log to console
let isHotOven = true;

// check if value is true and output a message to the console
if (isHotOven) {
  console.log("Careful, Oven is hot!");
} else {
  console.log("It's O.K., Oven is NOT hot!");
}

// check if value is not true using the negation operatore '!' and output a message to the console
isHotOven = false; // change boolean value
if (!isHotOven) {
  console.log("The oven broke so it's not hot");
} else {
  console.log("Watch Out, Oven is hot!");
}
